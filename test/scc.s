/* OPCODE : SCC*/
/*-----------------------------------------------------------*/
/*-----------------------------------------------------------*/

op_SCC:         move #0x01 , %ccr             
                scc (0x00010000)                   /* Clear the EA byte*/
                cmpi.b #0x00 , (0x00010000)
                bne TEST_FAIL

                move #0x00 , %ccr
                scc (0x00010000)                   /* Set the EA byte to 0xFF*/
                cmpi.b #0xFF , (0x00010000)
                bne TEST_FAIL

                rts
                


